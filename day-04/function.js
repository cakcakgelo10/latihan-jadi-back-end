// function
function salam(nama) {
    return `Halo, ${nama}!`;
}
console.log(salam("Reza")); // Output: Halo, Reza!
// ===============================================

// Function Expression
const salamm = function (nama) {
    return `Halo, ${nama}!`;
};
console.log(salamm("Reza")); // Output: Halo, Reza!
// ================================================

// Arrow function
const salammm = (nama) => `Halo, ${nama}!`;
console.log(salammm("Reza")); // Output: Halo, Reza!



