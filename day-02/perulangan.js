// perulangan for : Perulangan dengan jumlah iterasi yang diketahui.
for (let i = 1; i <= 5 ; i++) {
    console.log(`Iterasi ke-${i}`);
}

// while: Perulangan yang terus berjalan selama kondisi terpenuhi.
let j = 1;
while (j <= 5) {
    console.log(`Iterasi ke-${j}`);
    j++;
}

// do...while: Mirip dengan while, tetapi selalu dieksekusi setidaknya sekali.
let i = 1;
do {
    console.log(`Iterasi ke-${i}`);
    i++;
} while (i <= 5);